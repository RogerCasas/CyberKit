def lookup_vendor(mac: str) -> str:
    """Return vendor name for a MAC address, or 'Unknown'."""
    # Normalise: strip separators, uppercase, take first 6 chars
    clean = mac.replace(":", "").replace("-", "").replace(".", "").upper()
    if len(clean) < 6:
        return "Unknown"
    prefix = clean[:6]
    return OUI_TABLE.get(prefix, "Unknown")
